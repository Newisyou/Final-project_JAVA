public class Team {

    public String name;
    public String team;
    public String position;
    public int height;
    public int weight;
    public double age;

    public Team(String name, String team, String position, int height, int weight, double age) {
        this.name = name;
        this.team = team;
        this.position = position;
        this.height = height;
        this.weight = weight;
        this.age = age;
    }
}
